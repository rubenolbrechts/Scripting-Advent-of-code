#!/usr/bin/python3
################################################################################
# Python variables do not need explicit declaration to reserve memory space
# Declaration happens automatically when you assign a value to a variable
# Equal sign (=) used to assign values to variables (example-code-2-1.py)
################################################################################

myInteger = 10        # integer assignment
myFloat   = 9.9       # floating point
myString  = "Paco"    # string

print(myInteger)
print(myFloat)
print(myString)

print("\nType of these variables:")
print("==============")
print(myInteger, type(myInteger))
print(myFloat, type(myFloat))
print(myString, type(myString))